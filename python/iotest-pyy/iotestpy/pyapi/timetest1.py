import time

#======================无时区=====================================
print("======================无时区=====================================")
print(time.gmtime(0))

print(time.gmtime())

print(time.gmtime(time.time()))

print(time.time())

#======================本地时区=====================================
print("======================本地时区=====================================")

print(time.localtime())

#===========================================================
print("===========================================================")

print(time.strptime("2016-01-12 22:10:30","%Y-%m-%d %H:%M:%S"))

print(time.strftime("%Y-%m-%d %H:%M:%S",time.localtime()))

